const mongoose = require('mongoose')
// ===-------------------------------------------------------------------------------------=== //

// ===-------------------------------------------------------------------------------------=== //
// =-- Book Model
// ===-------------------------------------------------------------------------------------=== //
const bookSchema = mongoose.Schema({
  _id: mongoose.Schema.Types.ObjectId,
  title: String,
  author: String,
  numberOfPages: Number,
  releaseYear: Number
})
// ===-------------------------------------------------------------------------------------=== //

module.exports = mongoose.model('Book', bookSchema)
